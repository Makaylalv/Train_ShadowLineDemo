package com.example.train_shadowlinedemo.entity;

public class Film {
    //id
    private int filmId;
    //名字
    private String filmName;
    //英文名字
    private String filmEnglishname;
    //导演
    private String filmDirector;
    //主演
    private String filmTostar;
    //上映时间
    private String filmReleasetime;
    //产地
    private String filmProducercountry;
    //简介
    private String filmInfo;
    //照片
    private String filmImg;
    //类型
    private String filmType;
    //电影位置
    private int filmPosition;

    public int getFilmId() {
        return filmId;
    }

    public void setFilmId(int filmId) {
        this.filmId = filmId;
    }

    public String getFilmName() {
        return filmName;
    }

    public void setFilmName(String filmName) {
        this.filmName = filmName;
    }

    public String getFilmEnglishname() {
        return filmEnglishname;
    }

    public void setFilmEnglishname(String filmEnglishname) {
        this.filmEnglishname = filmEnglishname;
    }

    public String getFilmDirector() {
        return filmDirector;
    }

    public void setFilmDirector(String filmDirector) {
        this.filmDirector = filmDirector;
    }

    public String getFilmTostar() {
        return filmTostar;
    }

    public void setFilmTostar(String filmTostar) {
        this.filmTostar = filmTostar;
    }

    public String getFilmReleasetime() {
        return filmReleasetime;
    }

    public void setFilmReleasetime(String filmReleasetime) {
        this.filmReleasetime = filmReleasetime;
    }

    public String getFilmProducercountry() {
        return filmProducercountry;
    }

    public void setFilmProducercountry(String filmProducercountry) {
        this.filmProducercountry = filmProducercountry;
    }

    public String getFilmInfo() {
        return filmInfo;
    }

    public void setFilmInfo(String filmInfo) {
        this.filmInfo = filmInfo;
    }

    public String getFilmImg() {
        return filmImg;
    }

    public void setFilmImg(String filmImg) {
        this.filmImg = filmImg;
    }

    public String getFilmType() {
        return filmType;
    }

    public void setFilmType(String filmType) {
        this.filmType = filmType;
    }

    public int getFilmPosition() {
        return filmPosition;
    }

    public void setFilmPosition(int filmPosition) {
        this.filmPosition = filmPosition;
    }
}
